/******************************************************************************
* Name: GPIOConfig (pin, mode)
* Description: Configures the mode of the pin as INPUT/PULLUP or OUTPUT
* Arguments: pin and mode
* Returns: None
******************************************************************************/
void GPIOplus()
{
    dutycycle += 10;
    if(dutycycle<=100)
    {
        t = t + 25;
    }
    else
    {
        dutycycle = 100;
        t=255;
    }
}
/******************************************************************************
* Name: int GPIOPinRead(int Pin)
* Description: Reads the mode of the pin as INPUT or OUTPUT
* Arguments: pin
* Returns: a
******************************************************************************/
void GPIOminus()
{
    dutycycle -=10;
    if(dutycycle >=0)
    {
        t= t-26;
    }
    else
    {
        dutycycle = 0;
        t = 0;
    }
}
