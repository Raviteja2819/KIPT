/******************************************************************************
* Name: FSMInit()
* Description: state mode
* Arguments: None
* Returns: None
******************************************************************************/
void FSMInit(){
        GPIOPinWrite(PD6,HIGH);
        GPIOPinWrite(PD7,HIGH);
        _delay_ms(2000);
        GPIOPinWrite(PD7,LOW);
        GPIOPinWrite(PD6,LOW);
        _delay_ms(2000);
};
/******************************************************************************
* Name: FSMStateA()
* Description: FSM state mode A
* Arguments: None
* Returns: None
******************************************************************************/
void FSMStateA(){
        GPIOPinWrite(PD6,HIGH);
        GPIOPinWrite(PD7,HIGH);
    };
/******************************************************************************
* Name: FSMStateB()
* Description: FSM state B
* Arguments: None
* Returns: None
******************************************************************************/
void FSMStateB(){
        GPIOPinWrite(PD6,LOW);
        GPIOPinWrite(PD7,LOW);
    };
